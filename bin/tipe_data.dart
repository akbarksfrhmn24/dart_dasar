void main() {
  // tipe data number

  /**
   * Tipe data integer(bilangan bulat)
   * tipe data double(bilangan desimal)
   */
  int number1 = 21;
  double number2 = 2.1;

  print(number1);
  print(number2);

  // tipe data num(integer + double)
  num angka = 12;
  print(angka);

  angka = 1.2;
  print(angka);
}